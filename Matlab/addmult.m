function [s,p] = addmult(x,y)
% addmult: computes the sum and the product of two matrices
%
% [s,p] = addmult(x,y)
%
% Input Variables
% x = Matrix Input 1
% y = Matrix Input 2
%
% Output Variables
% s = sum of the matrices (s = x+y)
% p = product of the matrices ( p = x*y)
%
% Version 0.1 by Edward Fine

s = x+y;
p = x*y;open 